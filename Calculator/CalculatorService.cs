namespace Calculator
{
    /// <summary>
    /// Class for calculation services and utils
    /// </summary>
    public class CalculatorService
    {
        /// <summary>
        /// Add two 'double' numbers and return their sum.
        /// </summary>
        /// <param name="a">number 1</param>
        /// <param name="b">number 2</param>
        /// <returns>sum of number 1 and number 2.</returns>
        public double Add(double a, double b)
        {
            return a + b;
        }
    }
}
